lli caleven(lli n){    //give prefix uotp n
	//2^4^....^n
	if(n==0)return 0;
	lli tm = n%8;
	if(tm==0)return n;
	else if(tm==2)return 2;
	else if(tm==4)return n+2;
	else return 0;
}
lli calodd(lli n){
	if(n==1)return 1;
	return ((n >> 1) & 1 ^ (n&1 ? 1 : n))^(caleven(n-1));
}
lli fun(lli n){
	if(n&1)return calodd(n);
	else return caleven(n);
}
lli query(lli l,lli r){
	return fun(r)^fun(l-1);
}
