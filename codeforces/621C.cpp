lli get(lli l,lli r,lli p){
	lli rt = r/p;
	lli lt = lli(ceil(double(l)/double(p)));
	if(rt<lt)return 0;
	return rt-lt+1;
}
double fun(ii x,ii y,lli p){
	double ret =(double) (x.S-x.F+1-get(x.F,x.S,p))*(y.S-y.F+1-get(y.F,y.S,p));
	ret/=double((x.S-x.F+1)*(y.S-y.F+1));
	return double(1.0)-ret;
}
