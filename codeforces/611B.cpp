lli cnt(lli n){
	lli ret = 0;
	while(n){
		ret++;
		n=n>>1;
	}
	return ret;
}
lli cal(lli n){
	if(n==0)return 0;
	lli ret = 0;
	//lli bi = log2(n) + 1;
	lli bi = cnt(n);
	//	trace2(n,bi);
	lli cnt0 = bi - bitcount(n);
	if(cnt0==0)ret+=bi-1;
	else{
		lli tm = n;
		while(cnt(tm)-bitcount(tm)==1){
			tm=(tm>>1);
		}
		ret+=bitcount(tm);
		if(cnt0>1)ret-=1;
	}
	ret+=((bi-1)*(bi-2))/2;
	return ret;
}
