const int MAX = 100005;
char s[MAX],t[2*MAX];
int f[2*MAX],le;
void cal_f(){
	int j,l=2*le;
	j=f[0]=0;
	for(int i=1;i<l;++i){
		while(j>=0&&t[j]!=t[i]){
			if(j>=1)j=f[j-1];
			else j=-1;
		}
		++j;
		f[i]=j;
	}
}
void solve(){
	int l,k=0;
	for(int i=le-1;i>=0;i--)t[k++]=s[i];
	strcpy(t+k,s);
	cal_f();
	l=le-1-f[2*le-1];
	printf("%s",s);
	for(int i=l;i>=0;--i)printf("%c",s[i]);
	printf("\n");
}
int main(){
	while(scanf("%s",s)!=EOF){
		le=strlen(s);
		solve();
	}
	return 0;
}
